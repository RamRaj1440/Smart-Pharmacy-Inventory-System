      --   Rank Medicines by Revenue (RANK())
      
      SELECT 
      m.medicine_name,
      SUM(s.quantity * s.selling_price) AS total_revenue,
      RANK() OVER (
			ORDER BY SUM(s.quantity * s.selling_price) DESC
		)AS revenue_rank
        FROM medicines AS m
        JOIN sales AS s
           ON m.medicine_id = s.medicine_id
         GROUP BY
         m.medicine_id,
         m.medicine_name;
            
            
            -- Sequential Numbering (ROW_NUMBER())
            
        SELECT 
            medicine_name,
            unit_price,
            ROW_NUMBER() OVER (
					ORDER BY unit_price DESC
                    ) AS row_num
                 FROM medicines; 
                 
	-- Dense Ranking (DENSE_RANK())
    
    SELECT 
        medicine_name,
        unit_price,
        DENSE_RANK() OVER (
             ORDER BY unit_price DESC
             ) ASdense_rank
           FROM medicines;  
           
