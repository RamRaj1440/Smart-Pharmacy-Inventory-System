CREATE VIEW vw_inventory_status AS
SELECT 
    m.medicine_id,
    m.medicine_name,
    c.category_name,
    i.quantity_in_stock,
    i.reorder_level
   FROM medicines AS m
   JOIN categories AS c
       ON m.category_id = c.category_id
     JOIN inventory AS i
         ON m.medicine_id = i.medicine_id;
         
  -- Query the View
   SELECT *
    FROM vw_inventory_status;
    
    -- "Show me the total revenue generated by each medicine."
    
    CREATE VIEW vw_medicine_sales_summary AS
    SELECT 
    m.medicine_id,
    m.medicine_name,
    SUM(s.quantity) AS total_units_sold,
    SUM(s.quantity * s.selling_price) AS total_revenue
    FROM medicines AS m
    JOIN sales AS s
        ON m.medicine_id = s.medicine_id
      GROUP BY
      m.medicine_id,
      m.medicine_name;
      
      SELECT *
      FROM vw_medicine_sales_summary;
      
      
