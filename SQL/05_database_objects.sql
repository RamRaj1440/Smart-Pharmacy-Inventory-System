       AFTER INSERT ON sales
       FOR EACH ROW
       BEGIN
            UPDATE inventory
            SET quantity_in_stock = quantity_in_stock - NEW.quantity
            WHERE medicine_id = NEW.medicine_id;
          END$$
          
